package com.lk.qf.pay.beans;

public class BankBranch {
	private String subBranch;
	private String cnapsCode;
	private String bankbankName;
	private String bankbankId;
	
	public String getBankbankName() {
		return bankbankName;
	}
	public void setBankbankName(String bankbankName) {
		this.bankbankName = bankbankName;
	}
	public String getBankbankId() {
		return bankbankId;
	}
	public void setBankbankId(String bankbankId) {
		this.bankbankId = bankbankId;
	}
	public String getSubBranch() {
		return subBranch;
	}
	public void setSubBranch(String subBranch) {
		this.subBranch = subBranch;
	}
	public String getCnapsCode() {
		return cnapsCode;
	}
	public void setCnapsCode(String cnapsCode) {
		this.cnapsCode = cnapsCode;
	}

}
